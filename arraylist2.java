public class Daftar {

    int nomor;
    String judul;
    String pengarang;
    String penerbit;
    int tahun;
    int kategori;
  
    public Daftar(
      int nomor,
      String judul,
      String pengarang,
      String penerbit,
      int tahun,
      int kategori
    ) {
      this.nomor = nomor;
      this.judul = judul;
      this.pengarang = pengarang;
      this.penerbit = penerbit;
      this.tahun = tahun;
      this.kategori = kategori;
    }
  }